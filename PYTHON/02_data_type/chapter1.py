sugar_amount = 150
print(f"Initial sugar {sugar_amount}")

sugar_amount += 100
print(f"Sugar after adding {sugar_amount}")

sugar_amount -= 50
print(f"Sugar after removing {sugar_amount}")

sugar_amount *= 2
print(f"Sugar after doubling {sugar_amount}")

sugar_amount /= 2
print(f"Sugar after halving {sugar_amount}")

sugar_amount **= 2
print(f"Sugar after squaring {sugar_amount}")

sugar_amount %= 2
print(f"Sugar after modulo {sugar_amount}")


